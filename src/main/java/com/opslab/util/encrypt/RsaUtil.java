    /**
     * <p>
     * 获取私钥
     * </p>
     *
     * @param keyMap 密钥对
     * @return
     * @throws Exception
     */
    public static String getPrivateKey(Map<String, Object> keyMap) throws Exception {
        Key key = (Key) keyMap.get(PRIVATE_KEY);
        return encryptBASE64(key.getEncoded());
    }

    /**
     * <p>
     * 获取公钥
     * </p>
     *
     * @param keyMap 密钥对
     * @return
     * @throws Exception
     */
    public static String getPublicKey(Map<String, Object> keyMap) throws Exception {
        Key key = (Key) keyMap.get(PUBLIC_KEY);
        return encryptBASE64(key.getEncoded());
    }

    /**
     * java端私钥加密
     *
     * @param data
     * @param PRIVATEKEY
     * @return
     * @throws Exception
     */
    public static String encryptedDataWithPriKey(String data, String PRIVATEKEY) throws Exception {
        return encryptBASE64(encryptByPrivateKey(data.getBytes(), PRIVATEKEY));
    }


    /**
     * java端私钥解密
     *
     * @throws Exception
     */
    public static String decryptDataWithPubKey(String data, String PUBLICKEY) throws Exception {
        byte[] rs = decryptBASE64(data);
        return new String(decryptByPublicKey(rs, PUBLICKEY), Opslab.UTF_8);
    }


    /**
     * java端公钥加密
     *
     * @throws Exception
     */
